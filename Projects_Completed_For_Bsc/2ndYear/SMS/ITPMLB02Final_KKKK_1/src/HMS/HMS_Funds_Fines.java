    private void Fu_tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Fu_tblMouseClicked

//mouse clicking event on the table        
        
         int r = Fu_tbl.getSelectedRow();
        
        String FundID = Fu_tbl.getValueAt(r, 0).toString();
        String SHID = Fu_tbl.getValueAt(r, 1).toString();
        String Amount = Fu_tbl.getValueAt(r, 2).toString();
        String Submission_Date = Fu_tbl.getValueAt(r, 3).toString();//date chooser
        String Description = Fu_tbl.getValueAt(r, 4).toString();
     try{
        Date date=new SimpleDateFormat("MMM d,yyyy").parse(Submission_Date); //date chooser
        Fu_ID_txt.setText(FundID);
        Fu_SHID_txt.setText(SHID);
        Fu_Amnt_txt.setText(Amount);
        Fu_SumbitDate_DateCH.setDate(date);
        Fu_Desc_txt.setText(Description);
     }
     catch(Exception e){
        JOptionPane.showMessageDialog(null,e);
   }
    
    }//GEN-LAST:event_Fu_tblMouseClicked

