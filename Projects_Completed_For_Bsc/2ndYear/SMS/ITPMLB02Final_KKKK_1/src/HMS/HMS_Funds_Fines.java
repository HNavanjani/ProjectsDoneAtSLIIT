    private void Fu_ADD_butActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Fu_ADD_butActionPerformed
        try {
            String sql = "INSERT INTO hms_funds"
                    + "(SHID,Amount,Submission_Date,Description)"
                    + "VALUES (?,?,?,?)";

            pst = conn.prepareStatement(sql);
            
            
           

            pst.setString(1, Fu_SHID_txt.getText());

            pst.setString(2, Fu_Amnt_txt.getText());
            
            pst.setString(3, ((JTextField) Fu_SumbitDate_DateCH.getDateEditor().getUiComponent()).getText());

            pst.setString(4, Fu_Desc_txt.getText());

             
            
            
            

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Record Inserted Successfully !");
            
            tableload();
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

        
    }//GEN-LAST:event_Fu_ADD_butActionPerformed

