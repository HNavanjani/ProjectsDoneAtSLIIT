     public boolean isEmpty(){
      
        if(txt_py_sid.getText().isEmpty())
            req.setVisible(true);
        else
            req.setVisible(false);
        if(txt_py_nic.getText().isEmpty())
            req1.setVisible(true);
        else
            req1.setVisible(false);
        
        if(!txt_py_sid.getText().isEmpty()&&!txt_py_nic.getText().isEmpty())
            return true;
        else
        {
            JOptionPane.showMessageDialog(null, "Please Enter all the Values!");
            return false;
        }
       
        }
    
    
    
