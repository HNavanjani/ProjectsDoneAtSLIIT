    private void btn_py_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_py_loginActionPerformed
       if(isEmpty()){ 
    try{
        
            
        //conn = DriverManager.getConnection("jdbc:mysql://localhost/sims_db","root","");
        
        String sql = "SELECT * FROM student  WHERE  sid=? AND nic=?";

        pst=conn.prepareStatement(sql);
         
        pst.setString(1,txt_py_sid.getText());
        pst.setString(2,txt_py_nic.getText());
         
        rs=pst.executeQuery();
         
        if(rs.next()){
                    
                    String stdusname = rs.getString("stu_username");
                    Student.stuname = stdusname;
                    
                    int stuid = rs.getInt(1);
                    Student.stid=stuid;
                    
                    String stdnic = rs.getString("nic");
                    Student.snic = stdnic;
                    
                    String stdfname = rs.getString("stu_fname");
                    Student.sfname = stdfname;
                    
                    String stdlname = rs.getString("stu_lname");
                    Student.slname = stdlname;
                    
                    String stdgender = rs.getString("gender");
                    Student.sgender = stdgender;
                    
                    String stddob = rs.getString("dob");
                    Student.sdob = stddob;
                    
                    String stdemail = rs.getString("stu_email");
                    Student.semail = stdemail;
                    
                    int stdphone = rs.getInt(8);
                    Student.sphone=stdphone;
                    
                    String stdaddress = rs.getString("stu_address");
                    Student.saddress = stdaddress;
                    
                    String stdpnic = rs.getString("parent_nic");
                    Student.pnic = stdpnic;
                    
                    int stdpphone = rs.getInt(11);
                    Student.pphone=stdpphone;
                    
                    String stdprog = rs.getString("program");
                    Student.sprog = stdprog;
                    
                    String stdenroll = rs.getString("enroll_date");
                    Student.senroll = stdenroll;
                    
                    String stdsem = rs.getString("cur_sem");
                    Student.ssem = stdsem;
                    
                    byte[]imagedata=rs.getBytes("profile_pic");
                    format = new ImageIcon(imagedata);
                    Student.sprofilepic = format;
                    
                    
                    
                    
                    
                    
                    JOptionPane.showMessageDialog(null, "Successfully logged in...");
                    
                    dispose();
                    new Payment_Profile().setVisible(true);
                    
                    }
        else{
                    JOptionPane.showMessageDialog(null, "Username and password not matched !");
  
                    }
         
        }
        catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                    }

    }

    }//GEN-LAST:event_btn_py_loginActionPerformed

