    private void btn_lec_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_lec_loginActionPerformed
    if(isEmpty()){
    try{
            
        //conn = DriverManager.getConnection("jdbc:mysql://localhost/sims_db","root","");
        
        String sql = "SELECT * FROM lecturer WHERE lec_username=? AND lec_password=?";

        pst=conn.prepareStatement(sql);
         
        pst.setString(1,txt_lec_username.getText());
        pst.setString(2,txt_lec_pw.getText());
         
        rs=pst.executeQuery();
         
        if(rs.next()){
                    
                    String lecusname = rs.getString("lec_username");
                    Lecturer.luname = lecusname;
                    
                    int lecid = rs.getInt(1);
                    Lecturer.lecid=lecid;
                    
                    String lecnic = rs.getString("nic");
                    Lecturer.lnic = lecnic;
                    
                    String lecfname = rs.getString("lec_fname");
                    Lecturer.lfname = lecfname;
                    
                    String leclname = rs.getString("lec_lname");
                    Lecturer.llname = leclname;
                    
                    String lecgender = rs.getString("gender");
                    Lecturer.lgender = lecgender;
                    
                   
                    String lecemail = rs.getString("lec_email");
                    Lecturer.lemail = lecemail;
                    
                    int lecphone = rs.getInt(7);
                    Lecturer.lphone=lecphone;
                    
                    String lecaddress = rs.getString("lec_address");
                    Lecturer.laddress = lecaddress;
                    
                    
                    String lecposition = rs.getString("position");
                    Lecturer.lposition = lecposition;
                    
                    String lecmodule = rs.getString("module");
                    Lecturer.lmodule = lecmodule;
                    
                    String lecenroll = rs.getString("enroll_date");
                    Lecturer.lenroll = lecenroll;
                    
                    
                    byte[]imagedata=rs.getBytes("lec_profile_pic");
                    format = new ImageIcon(imagedata);
                    Lecturer.lprofilepic = format;
                    
                    
                    
                    JOptionPane.showMessageDialog(null, "Successfully logged in...");
                    
                    dispose();
                    new Lecturer_Profile().setVisible(true);
                    
                    }
        else{
                    JOptionPane.showMessageDialog(null, "Username and password not matched !");
  
                    }
         
        }
        catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                    }

    }

    }//GEN-LAST:event_btn_lec_loginActionPerformed

