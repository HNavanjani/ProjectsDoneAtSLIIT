    public boolean isEmpty(){
      
        if(txt_lec_username.getText().isEmpty())
            req.setVisible(true);
        else
            req.setVisible(false);
        if(txt_lec_pw.getText().isEmpty())
            req1.setVisible(true);
        else
            req1.setVisible(false);
        
        if(!txt_lec_username.getText().isEmpty()&&!txt_lec_pw.getText().isEmpty())
            return true;
        else
        {
            JOptionPane.showMessageDialog(null, "Please Enter all the Values!");
            return false;
        }
       
        }
    
    
    
    
