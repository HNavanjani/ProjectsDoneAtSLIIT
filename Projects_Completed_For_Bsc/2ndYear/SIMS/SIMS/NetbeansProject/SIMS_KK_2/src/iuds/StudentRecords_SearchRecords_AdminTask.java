      public boolean validnic(){
        
        String nic = "^[0-9]{9}V$";
        
        Pattern patternnic = Pattern.compile(nic,Pattern.CASE_INSENSITIVE);
        
        Matcher regeMatchernic = patternnic.matcher(txt_stu_nic.getText());
        if(!regeMatchernic.matches()){
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Enter a valid NIC !");
            return false;
        }
        else 
            return true;
      }
        
