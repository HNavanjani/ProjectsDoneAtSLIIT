    private void txt_stu_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_stu_searchKeyReleased

        

        try{
            String sql="SELECT * FROM student WHERE sid=?";

            pst=conn.prepareStatement(sql);
            pst.setString(1,txt_stu_search.getText());
            rs=pst.executeQuery();
            
            if(rs.next()){
                String a1 = rs.getString("sid");
                txt_sid.setText(a1);
                String a2 = rs.getString("nic");
                txt_stu_nic.setText(a2);
                String a3 = rs.getString("stu_fname");
                txt_stu_fname.setText(a3);
                String a4 = rs.getString("stu_lname");
                txt_stu_lname.setText(a4);
                String a5 = rs.getString("gender");
                txt_stu_gender.setText(a5);
                String a6 = rs.getString("dob");
                txt_stu_dob.setText(a6);
                String a7 = rs.getString("stu_email");
                txt_stu_email.setText(a7);
                String a8 = rs.getString("stu_phone");
                txt_stu_phone.setText(a8);
                String a9 = rs.getString("stu_address");
                txt_stu_address.setText(a9);
                String a10 = rs.getString("parent_nic");
                txt_parent_nic.setText(a10);
                String a11 = rs.getString("parent_contact_no");
                txt_parent_phone.setText(a11);
                String a12 = rs.getString("program");
                txt_stu_prog.setText(a12);
                String a13 = rs.getString("enroll_date");
                txt_stu_edate.setText(a13);
                String a14 = rs.getString("cur_sem");
                txt_stu_asem.setText(a14);
                
                
                
            byte[]imagedata=rs.getBytes("profile_pic");
            format = new ImageIcon(new ImageIcon(imagedata).getImage().getScaledInstance(lbl_photo.getWidth(), lbl_photo.getHeight(), Image.SCALE_DEFAULT));
            lbl_photo.setIcon(format);
                
                
            }
            

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);

        }

    }//GEN-LAST:event_txt_stu_searchKeyReleased

